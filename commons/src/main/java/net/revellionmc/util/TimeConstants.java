package net.revellionmc.util;

/**
 * OutdatedVersion
 * Jan/02/2017 (10:31 PM)
 */

public class TimeConstants
{

    /** number of milliseconds in {@code one Minecraft tick} */
    public static final long TICK = 50;

    /** number of milliseconds in {@code one second} */
    public static final long SECOND = 1000;

    /** number of milliseconds in {@code one minute} */
    public static final long MINUTE = SECOND * 60;

    /** number of milliseconds in {@code one hour} */
    public static final long HOUR = MINUTE * 60;

    /** number of milliseconds in {@code one day} */
    public static final long DAY = HOUR * 24;

    /** number of milliseconds in {@code one week} */
    public static final long WEEK = DAY * 7;

}
